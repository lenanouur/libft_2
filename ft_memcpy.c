#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char *d;
	unsigned char *s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	while (n > 0 && dst != src)
	{
		*d++ = *s++;
		n--;
	}
	return (dst);
}
